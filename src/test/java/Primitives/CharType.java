package Primitives;

public class CharType {
    public static void main(String[] args) {

        char letter = 77;
        //char data type will find the character in ASCII table that has a value 77.

        System.out.println(letter );// This will print the character 'M' as the values of character M is 77

        char letter2 ='T';
        //char data type will be assigned that character 'T'.

        System.out.println(letter2);
        // T is result

        char ch='a';
        // the numerical value of character a is 97
        System.out.println(ch+10);
        //107
        System.out.println(ch + " text");
        // a text





    }
}
